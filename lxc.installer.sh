# 00 # PRE FUNCTION # ------------------------------------------------------------------------------------------------------------------------------------- #
function pre {
    # FORCE TO USE IPV4 IN APT # -------------------------------------------------------------------------------------------------------------------------- #
    echo -e 'Acquire::ForceIPv4 "true";' > /etc/apt/apt.conf.d/99force-ipv4
    # ----------------------------------------------------------------------------------------------------------------------------------------------------- #


    # UPDATE AND UPGRADE SYSTEM # ------------------------------------------------------------------------------------------------------------------------- #
    apt-get update
    apt-get -y dist-upgrade
    apt -y autoremove
    apt-get -y -f install
    apt-get clean
    apt-get install -y lsb-release &> /dev/null
    # ----------------------------------------------------------------------------------------------------------------------------------------------------- #
}


# 01 # INSTALL LXC v4.0.6 FROM SOURCE CODE IN FOCAL # ----------------------------------------------------------------------------------------------------- #
function source_focal_4 {
    # INSTALL PRE PACKAGES # ------------------------------------------------------------------------------------------------------------------------------ #
    apt-get install -y make git
    apt-get install -y libtool m4 automake pkg-config
    apt-get install -y libvirt0 libpam-cgfs bridge-utils uidmap
    # ----------------------------------------------------------------------------------------------------------------------------------------------------- #


    # GIT CLONE PROJECT # --------------------------------------------------------------------------------------------------------------------------------- #
    cd /tmp/
    rm -rf lxc &> /dev/null
    git clone https://github.com/lxc/lxc.git
    [ ! -d lxc ] && echo "[>] cannot access 'lxc': No such file or directory" && exit 1
    # ----------------------------------------------------------------------------------------------------------------------------------------------------- #


    # CHANGE DIRECTORY # ---------------------------------------------------------------------------------------------------------------------------------- #
    cd lxc
    # ----------------------------------------------------------------------------------------------------------------------------------------------------- #


    # CHANGE BRANCH AND TAG # ----------------------------------------------------------------------------------------------------------------------------- #
    git checkout lxc-4.0.6 -b remotes/origin/stable-4.0
    # ----------------------------------------------------------------------------------------------------------------------------------------------------- #


    # RUN autogen.sh SCRIPT # ----------------------------------------------------------------------------------------------------------------------------- #
    ./autogen.sh
    [ "$?" != "0" ] && echo "[>] ERROR IN autogen.sh" && exit 1
    # ----------------------------------------------------------------------------------------------------------------------------------------------------- #


    # RUN configure SCRIPT # ------------------------------------------------------------------------------------------------------------------------------ #
    ./configure
    [ "$?" != "0" ] && echo "[>] ERROR IN configure" && exit 1
    # ----------------------------------------------------------------------------------------------------------------------------------------------------- #


    # RUN MAKE COMMAND # ---------------------------------------------------------------------------------------------------------------------------------- #
    make
    [ "$?" != "0" ] && echo "[>] ERROR IN make" && exit 1
    # ----------------------------------------------------------------------------------------------------------------------------------------------------- #


    # RUN MAKE INSTALL COMMAND # -------------------------------------------------------------------------------------------------------------------------- #
    make install
    [ "$?" != "0" ] && echo "[>] ERROR IN make install" && exit 1
    # ----------------------------------------------------------------------------------------------------------------------------------------------------- #


    # CHANGE BASHRC # ------------------------------------------------------------------------------------------------------------------------------------- #
    echo -e 'export LD_LIBRARY_PATH=$LD_LIBRARY_PATH:/usr/local/lib' >> /root/.bashrc
    # ----------------------------------------------------------------------------------------------------------------------------------------------------- #


    # RELOAD BASHRC # ------------------------------------------------------------------------------------------------------------------------------------- #
    . /root/.bashrc
    # ----------------------------------------------------------------------------------------------------------------------------------------------------- #


    # CLEAN UP TERMINAL # --------------------------------------------------------------------------------------------------------------------------------- #
    clear
    # ----------------------------------------------------------------------------------------------------------------------------------------------------- #


    # SHOW LXC VERSION # ---------------------------------------------------------------------------------------------------------------------------------- #
    echo -en "[>] LXC VERSION : "
    lxc-ls --version
    # ----------------------------------------------------------------------------------------------------------------------------------------------------- #
}


